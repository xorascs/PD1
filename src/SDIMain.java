    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String login = (String) obj.get("Lietotājvārds");
        String password = (String) obj.get("Parole");
        
        String adl = Admin.login;
        String adpass = Admin.password;
        
        if (jTextField6.getText().equals(login) && jTextField7.getText().equals(password)) {
            jDialog2.setBounds(450, 450, 650, 450);
            jDialog2.setVisible(true);
            jDialog1.setVisible(false);
        }
        else if (jTextField6.getText().equals(adl) && jTextField7.getText().equals(adpass)) {
            JOptionPane.showMessageDialog(null, "You are logged on as Admin.");
            jDialog2.setBounds(450, 450, 400, 350);
            jDialog2.setVisible(true);
            jDialog1.setVisible(false);
            ent = true;
        }
        else if (!jTextField6.getText().equals(login) || !jTextField7.getText().equals(password)) {
            JOptionPane.showMessageDialog(null, "Uncorrect login or password.", "Alert", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

