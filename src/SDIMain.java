    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

       String[] text = {
                         "Vārds", jTextField1.getText(),
                         "Uzvārds", jTextField2.getText(),
                         "Lietotājvārds", jTextField3.getText(),
                         "Parole", jTextField4.getText(),
                         "Parole (atkārtoti)", jTextField5.getText()
                        };   
       
           try {
                if (obj.get("Vārds").equals(jTextField1.getText()) || obj.get("Uzvārds").equals(jTextField2.getText()) || obj.get("Lietotājvārds").equals(jTextField3.getText())) {
                    JOptionPane.showMessageDialog(null, "User with same data already exist.", "Alert", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                } catch (Exception e) {}                
       
           if ("".equals(jTextField1.getText()) || "".equals(jTextField2.getText()) || "".equals(jTextField3.getText()) || "".equals(jTextField4.getText()) || "".equals(jTextField5.getText())) {
               JOptionPane.showMessageDialog(null, "Please complete all areas.", "Alert", JOptionPane.ERROR_MESSAGE);
               return;
           }
           
           if (!jTextField4.getText().equals(jTextField5.getText())) {
               JOptionPane.showMessageDialog(null, "Passwords are not the same.", "Alert", JOptionPane.ERROR_MESSAGE);
           }
           
           else if (jCheckBox1.isSelected()) {
                try {
                obj.put(text[0], text[1]);
                obj.put(text[2], text[3]);
                obj.put(text[4], text[5]);
                obj.put(text[6], text[7]);
                obj.put(text[8], text[9]);
                employeeList.add(obj);
                JOptionPane.showMessageDialog(null, "You successfully passed registration.");
                
                } catch (Exception e) {
                    e.printStackTrace();
                }

                try {
                FileWriter file = new FileWriter("Registrations.json");
                file.append(employeeList.toJSONString());
                file.flush();

                } catch (Exception e) {
                    e.printStackTrace();
                }
                jDialog1.setBounds(450, 450, 650, 450);
                jDialog1.setVisible(true);
                }
        else {
           JOptionPane.showMessageDialog(null, "Please complete all areas.", "Alert", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

